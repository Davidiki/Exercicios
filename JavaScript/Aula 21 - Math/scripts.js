/* 
A biblioteca Math é usada para realizar operações matemáticas comuns, como cálculos trigonométricos, arredondamentos, geração de números aleatórios e muito mais.

Aqui estão algumas das funções e constantes disponíveis na biblioteca Math:

Funções Trigonométricas:

Math.sin(x): Retorna o seno de x (em radianos).
Math.cos(x): Retorna o cosseno de x (em radianos).
Math.tan(x): Retorna a tangente de x (em radianos).
Math.atan2(y, x): Retorna o ângulo, em radianos, cuja tangente é y / x.
Funções Exponenciais e Logarítmicas:

Math.pow(base, expoente): Retorna a base elevada ao expoente.
Math.sqrt(x): Retorna a raiz quadrada de x.
Math.exp(x): Retorna o valor de e (número de Euler) elevado a x.
Math.log(x): Retorna o logaritmo natural (base e) de x.
Funções de Arredondamento:

Math.round(x): Retorna o valor de x arredondado para o número inteiro mais próximo.
Math.floor(x): Retorna o maior inteiro menor ou igual a x.
Math.ceil(x): Retorna o menor inteiro maior ou igual a x.
Funções de Números Aleatórios:

Math.random(): Retorna um número pseudoaleatório entre 0 (inclusivo) e 1 (exclusivo).
Math.floor(Math.random() * n): Retorna um número inteiro aleatório entre 0 e n-1.
Constantes:

Math.PI: Retorna o valor de pi (aproximadamente 3.141592653589793).
Math.E: Retorna o número de Euler (aproximadamente 2.718281828459045).
*/